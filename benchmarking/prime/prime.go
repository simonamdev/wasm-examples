package prime

// Prime - generates all prime numbers up to N
// https://stackoverflow.com/questions/21854191/generating-prime-numbers-in-go
func Prime(N int) (primes []int) {
	b := make([]bool, N)
	for i := 2; i < N; i++ {
		if b[i] == true {
			continue
		}
		primes = append(primes, i)
		for k := i * i; k < N; k += i {
			b[k] = true
		}
	}
	return
}
